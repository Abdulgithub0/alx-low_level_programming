#include "3-calc.h"

/**
 * op_add - return sum of two integers
 * @a: first arg
 * @b: second arg
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return ((a + b));
}

/**
 * op_sub - subtract two numbers
 * @a: first agr
 * @b: second arg
 *
 * Return: subtracted value
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first arg
 * @b: second arg
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: first arg
 * @b: second arg
 *
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two number
 * @a: first arg
 * @b: second arg
 *
 * Return: the result of modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}

